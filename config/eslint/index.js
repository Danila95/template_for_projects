// ============================================================================
// СОЗДАНИЕ КОНФИГУРАЦИИ ESLINT
// ============================================================================
// Централизованная функция для создания конфигурации ESLint Flat Config
// Возвращает массив конфигураций
// ============================================================================

/**
 * Создать полную конфигурацию ESLint
 * @returns {import('./types.js').ESLintConfig} массив конфигураций ESLint (Flat Config формат)
 */
export function createESLintConfig() {
	return [
		// ==========================================================================
		// 1. ИГНОРИРУЕМЫЕ ФАЙЛЫ
		// ==========================================================================
		// Файлы и директории, которые ESLint не должен проверять
		// ==========================================================================
		getIgnoresConfig(),

		// ==========================================================================
		// 2. БАЗОВАЯ КОНФИГУРАЦИЯ
		// ==========================================================================
		// Рекомендуемые правила JavaScript и базовые настройки
		// ==========================================================================
		getBaseConfig(),

		// ==========================================================================
		// 3. TYPESCRIPT КОНФИГУРАЦИЯ
		// ==========================================================================
		// Правила TypeScript и типизации
		// ==========================================================================
		...getTypeScriptConfig(),

		// ==========================================================================
		// 4. REACT КОНФИГУРАЦИЯ
		// ==========================================================================
		// Правила React, React Hooks и Fast Refresh
		// ==========================================================================
		...getReactConfig(),

		// ==========================================================================
		// 5. IMPORT КОНФИГУРАЦИЯ
		// ==========================================================================
		// Правила для импортов модулей
		// ==========================================================================
		getImportConfig(),

		// ==========================================================================
		// 6. JSX A11Y КОНФИГУРАЦИЯ
		// ==========================================================================
		// Правила доступности для JSX
		// ==========================================================================
		getJsxA11yConfig(),

		// ==========================================================================
		// 7. PRETTIER КОНФИГУРАЦИЯ
		// ==========================================================================
		// Интеграция Prettier с ESLint (должна быть последней)
		// ==========================================================================
		getPrettierConfig(),
	]
}

